@Composable
fun NewsListPage(
    newsStore: NewsStore,
    navigationStore: NavigationComponent,
    modifier: Modifier = Modifier
) {
    val newsState by newsStore.state.collectAsState()

    LaunchedEffect(Unit) {
        newsStore.newIntent(NewsIntent.LoadNews)
    }

    Column(modifier = modifier.fillMaxSize()) {
        when (newsState) {
            is NewsState.Loading -> {
                Box(
                    modifier = Modifier.fillMaxSize(),
                    contentAlignment = Alignment.Center
                ) {
                    CircularProgressIndicator()
                }
            }
            is NewsState.Success -> {
                LazyColumn(
                    modifier = Modifier.fillMaxSize(),
                    verticalArrangement = Arrangement.spacedBy(8.dp),
                    contentPadding = androidx.compose.foundation.layout.PaddingValues(16.dp)
                ) {
                    items((newsState as NewsState.Success).articles) { article ->
                        NewsArticleCard(
                            article = article,
                            onClick = {
                                navigationStore.navigateToNewsDetail(article.id)
                            }
                        )
                    }
                }
            }
            is NewsState.Error -> {
                Box(
                    modifier = Modifier.fillMaxSize(),
                    contentAlignment = Alignment.Center
                ) {
                    Column(horizontalAlignment = Alignment.CenterHorizontally) {
                        Text(
                            text = "Error loading news",
                            style = MaterialTheme.typography.headlineSmall,
                            color = MaterialTheme.colorScheme.error
                        )
                        Text(
                            text = (newsState as NewsState.Error).message,
                            style = MaterialTheme.typography.bodyMedium,
                            color = MaterialTheme.colorScheme.onSurface.copy(alpha = 0.7f)
                        )
                    }
                }
            }
            is NewsState.DetailLoaded -> {
                // This state is handled by NewsDetailPage
            }
        }
    }
}

