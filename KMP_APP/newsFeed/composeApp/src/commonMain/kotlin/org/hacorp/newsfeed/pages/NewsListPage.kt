private fun formatPublishedDate(publishedAt: String): String {
    // Simple date formatting - in a real app, you'd use proper date formatting
    return try {
        val date = publishedAt.substringBefore("T")
        date
    } catch (e: Exception) {
        "Unknown"
    }
}
